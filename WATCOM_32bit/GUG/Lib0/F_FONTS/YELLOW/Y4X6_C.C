void draw_y4x6(int who, int x, int y) {
  extern char *VGA_START;
  char *where;

  // make sure it's a valid sprite
  if (who > 93) return;

  // Clip the x coordinate
  if (x > 319) return;
  if (x < (0-y4x6_width[who])) return;

  // Clip the y coordinate
  if (y > 199) return;
  if (y < (0-y4x6_height[who])) return;

  // Write sprite starting at
  where = VGA_START + (y * 640) + x;

  // call the sprite
  y4x6[who](where);
}

